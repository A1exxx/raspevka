// songs.js — простые ОРИГИНАЛЬНЫЕ мелодии (не защищены авторским правом) для режима
// «Песни». Поются на «Ля», идут через тот же нотный хайвей (renderGame). seq: [offset, beats].

function song(id, name, tempo, seq) {
  return {
    id, name, tempo, syllable: 'Ля',
    make(root) {
      return {
        id, name, syllable: 'Ля', tempo, kind: 'song', root,
        desc: 'Простая мелодия — веди голос по нотам и попадай в каждую.',
        how: 'Пой на «ля», спокойно следуя за нотами. Это не упражнение, а маленькая песня.',
        notes: seq.map(([o, b]) => ({ midi: root + o, beats: b })),
      };
    },
  };
}

