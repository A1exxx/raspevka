export function renderGuide(app, { onExit, onSettings }) {
  const cards = SECTIONS.map((s) => `
    <div class="card guide-card">
      <div class="guide-head"><span class="guide-ic">${s.icon}</span><b>${s.title}</b></div>
      <p class="how">${s.body}</p>
    </div>`).join('');
  app.innerHTML = `
    <div class="screen catalog">
      <div class="game-top"><button class="icon-btn" id="back">‹ Меню</button></div>
      <div class="brand"><h1>Как тут всё устроено</h1>
        <p>Короткий гид: что где находится и что подкрутить, если что-то не получается.</p></div>
      ${cards}
      ${onSettings ? '<button class="btn btn-primary" id="toSettings" style="width:100%">Открыть настройки</button>' : ''}
      <p class="hint">Гид всегда под лампочкой 💡 на главном экране.</p>
    </div>
  `;
  document.getElementById('back').addEventListener('click', onExit);
  const ts = document.getElementById('toSettings');
  if (ts && onSettings) ts.addEventListener('click', onSettings);
}
