// Премиум-ценность: пользователь чувствует, что учится, а не просто «тренькает».

export const THEORY = [
  { t: 'Опора дыхания', b: 'Вдох — живот мягко наполняется (плечи не поднимаются). На выдохе живот плавно поджимается и «держит» звук ровным. Это фундамент: без опоры голос дрожит и быстро устаёт.' },
  { t: 'Звук «в маску»', b: 'Направляй звук в область носа и скул — голос начинает звенеть, а не «застревать» в горле. Поймать ощущение помогает мычание «м-м-м».' },
  { t: 'Зевок в горле', b: 'Лёгкое ощущение начала зевка освобождает гортань и расширяет пространство во рту. Звук становится объёмнее и свободнее, уходит зажим.' },
  { t: 'Не дави на верх', b: 'Высокие ноты берутся не силой, а лёгкостью и опорой. Давишь — связки зажимаются и можно сорвать голос. Расширение диапазона — это недели, не один день.' },
  { t: 'Мягкая атака', b: 'Начинай ноту мягко, без толчка горлом. Представь, что звук «вытекает», а не «выстреливает». Это бережёт связки и звучит красивее.' },
  { t: 'Округляй гласные', b: 'Пой гласные округло, будто внутри звучит «о». Это выравнивает тембр по всему диапазону и убирает резкость и «плоскость».' },
  { t: 'Губной тренаж «бррр»', b: 'Вибрация губами снимает зажим и выравнивает поток воздуха. Лучшая разминка перед пением — и проверка, что дыхание ровное.' },
  { t: 'Береги голос', b: 'Связки любят воду и отдых. Не пой на больном горле, делай паузы, не больше 20–30 минут подряд. Боль — всегда сигнал «стоп».' },
];

export function renderTheory(app, { onExit }) {
  let i = 0;
  function draw() {
    const c = THEORY[i];
    const dots = THEORY.map((_, k) => `<span class="dot ${k === i ? 'now' : k < i ? 'done' : ''}"></span>`).join('');
    app.innerHTML = `
      <div class="screen">
        <div class="game-top"><button class="icon-btn" id="back">‹ Меню</button></div>
        <div class="brand"><h1>Теория голоса</h1><p>Короткие уроки — по одному в день достаточно.</p></div>
        <div class="card theory-card">
          <div class="th-num">${i + 1} / ${THEORY.length}</div>
          <h3 class="th-title">${c.t}</h3>
          <p class="th-body">${c.b}</p>
        </div>
        <div class="progress-dots">${dots}</div>
        <div class="row">
          <button class="btn btn-ghost" id="prev" ${i === 0 ? 'disabled' : ''}>Назад</button>
          <button class="btn btn-primary" id="next">${i === THEORY.length - 1 ? 'Готово' : 'Далее'}</button>
        </div>
      </div>`;
    document.getElementById('back').addEventListener('click', onExit);
    document.getElementById('prev').addEventListener('click', () => { if (i > 0) { i--; draw(); } });
    document.getElementById('next').addEventListener('click', () => {
      if (i < THEORY.length - 1) { i++; draw(); } else onExit();
    });
  }
  draw();
}
